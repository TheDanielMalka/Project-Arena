# ── Cross-game isolation ──────────────────────────────────────────────────────

class TestValorantCrossGameIsolation:
    """
    Cross-game isolation is enforced at the ROUTER level: detect_result()
    dispatches to the correct detector based on the `game` parameter.
    The individual colour detectors share some hue ranges (Valorant teal H 75-105
    overlaps with CS2 green H 35-85 at H 75-85; Valorant crimson overlaps with
    CS2 red).  That overlap is intentional — game routing prevents cross-trigger
    in production.  These tests verify the router contract, not colour exclusivity.
    """

    def test_valorant_victory_detected_with_correct_game_param(self):
        """Valorant VICTORY must be detected when game='Valorant'."""
        path = _tpl("valorant_1920x1080_victory.png")
        result, conf = detect_result(path, game="Valorant")
        assert result == "victory"

    def test_valorant_defeat_detected_with_correct_game_param(self):
        """Valorant DEFEAT must be detected when game='Valorant'."""
        path = _tpl("valorant_1920x1080_defeat.png")
        result, conf = detect_result(path, game="Valorant")
        assert result == "defeat"

    def test_game_param_routes_to_different_detectors(self):
        """
        Passing game='CS2' must invoke the CS2 detector (different code path).
        We verify this by checking the Valorant VICTORY template under CS2
        routing: CS2 green H 35-85 partially overlaps Valorant teal H 75-105
        at H 75-85, so the result may differ between game params — the important
        thing is that the routing itself works without crashing.
        """
        path = _tpl("valorant_1920x1080_victory.png")
        val_result, _val_conf = detect_result(path, game="Valorant")
        cs2_result, _cs2_conf = detect_result(path, game="CS2")
        # Valorant detector must return "victory" for this template
        assert val_result == "victory"
        # CS2 detector call must not crash (result may differ — that is correct)


# ── Direct _detect_result_valorant ───────────────────────────────────────────

class TestDirectValorantDetector:

    def test_missing_file_returns_none(self):
        result, conf = _detect_result_valorant("/no/such/file.png")
        assert result is None
        assert conf == 0.0

    def test_custom_region_victory(self, tmp_path):
        """Custom region crop still detects victory on a solid-teal image."""
        import cv2
        import numpy as np
        # Solid teal: H=90, S=200, V=160
        hsv = np.full((200, 400, 3), (90, 200, 160), dtype=np.uint8)
        bgr = cv2.cvtColor(hsv, cv2.COLOR_HSV2BGR)
        img_path = str(tmp_path / "solid_teal.png")
        cv2.imwrite(img_path, bgr)
        result, conf = _detect_result_valorant(img_path, region=(0, 0, 400, 200))
        assert result == "victory"
        assert conf > 0.20

    def test_custom_region_defeat(self, tmp_path):
        """Custom region crop detects defeat on a dark-crimson image (V=45)."""
        import cv2
        import numpy as np
        # Dark crimson: H=5, S=150, V=45 — the key calibration case
        hsv = np.full((200, 400, 3), (5, 150, 45), dtype=np.uint8)
        bgr = cv2.cvtColor(hsv, cv2.COLOR_HSV2BGR)
        img_path = str(tmp_path / "dark_crimson.png")
        cv2.imwrite(img_path, bgr)
        result, conf = _detect_result_valorant(img_path, region=(0, 0, 400, 200))
        assert result == "defeat"
        assert conf > 0.15

    def test_defeat_dark_background_not_missed(self, tmp_path):
        """
        Regression: V=45 crimson was missed by the old V≥60 threshold.
        Verify the fix (V≥25) now detects it correctly.
        """
        import cv2
        import numpy as np
        # Very dark crimson, V=35 — represents the darkest part of the DEFEAT bg
        hsv = np.full((400, 800, 3), (5, 140, 35), dtype=np.uint8)
        bgr = cv2.cvtColor(hsv, cv2.COLOR_HSV2BGR)
        img_path = str(tmp_path / "very_dark_crimson.png")
        cv2.imwrite(img_path, bgr)
        result, conf = _detect_result_valorant(img_path, region=(0, 0, 800, 400))
        assert result == "defeat", (
            "Dark crimson (V=35) must be detected as defeat — "
            "old V≥60 threshold would have missed this"
        )

    def test_neutral_image_returns_none(self, tmp_path):
        """A grey image must not trigger either detector."""
        import cv2
        import numpy as np
        grey = np.full((200, 400, 3), 128, dtype=np.uint8)
        img_path = str(tmp_path / "grey.png")
        cv2.imwrite(img_path, grey)
        result, conf = _detect_result_valorant(img_path, region=(0, 0, 400, 200))
        assert result is None
        assert conf == 0.0
