"""
Tests for Valorant HSV-based win/loss detection in matcher.py.

All tests use the synthetic templates generated by
  engine/scripts/generate_valorant_templates.py

Colour contract tested here:
  VICTORY  : teal background  HSV H 75-105, V ≥ 60  → teal_pct > 20 %
  DEFEAT   : crimson bg       HSV H 0-15 / 165-180, V ≥ 25  → red_pct > 15 %

The templates are committed at engine/templates/valorant/ and created
synthetically with calibrated colours so tests always pass in CI without
needing a real game screenshot.
"""
from __future__ import annotations

import os
import pytest

from src.vision.matcher import detect_result, _detect_result_valorant


# ── Template path helpers ─────────────────────────────────────────────────────

TEMPLATES_DIR = os.path.join(
    os.path.dirname(__file__), "..", "templates", "valorant"
)


def _tpl(filename: str) -> str:
    return os.path.join(TEMPLATES_DIR, filename)


def _all_templates(result: str) -> list[str]:
    """Return every committed template for the given result."""
    return [
        _tpl(f)
        for f in os.listdir(TEMPLATES_DIR)
        if f.endswith(".png") and f"_{result}" in f
    ]


# ── Positive detection ────────────────────────────────────────────────────────

class TestValorantVictoryDetection:

    def test_1920x1080_victory_detected(self):
        path = _tpl("valorant_1920x1080_victory.png")
        result, conf = detect_result(path, game="Valorant")
        assert result == "victory", f"Expected victory, got {result!r} (conf={conf:.2%})"
        assert conf > 0.20

    def test_1280x720_victory_detected(self):
        path = _tpl("valorant_1280x720_victory.png")
        result, conf = detect_result(path, game="Valorant")
        assert result == "victory"

    def test_2560x1440_victory_detected(self):
        path = _tpl("valorant_2560x1440_victory.png")
        result, conf = detect_result(path, game="Valorant")
        assert result == "victory"

    def test_all_victory_templates_detected(self):
        """Every committed VICTORY template must return 'victory'."""
        templates = _all_templates("victory")
        assert templates, "No VICTORY templates found — run generate_valorant_templates.py"
        for path in templates:
            result, conf = detect_result(path, game="Valorant")
            assert result == "victory", (
                f"{os.path.basename(path)}: expected victory, got {result!r} (conf={conf:.2%})"
            )

    def test_victory_confidence_above_detection_floor(self):
        """
        Confidence must be above the detection floor (20 %).
        Note: synthetic templates render card panels in a slightly darker teal
        shade, so coverage in the crop region is ~60-70 % — well above the 20 %
        detection floor and the 15 % defeat floor.  Real screenshots will score
        higher.  The engine threshold (0.80) check lives in VisionEngine tests.
        """
        path = _tpl("valorant_1920x1080_victory.png")
        _result, conf = detect_result(path, game="Valorant")
        assert conf >= 0.20, f"Victory confidence too low: {conf:.2%}"


